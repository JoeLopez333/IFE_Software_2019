void send(int a){
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);
    uint8_t message[1];
    message[0] = a & 0xFF;
    HAL_SPI_Transmit(&hspi1, (uint8_t *)message, sizeof(message), HAL_MAX_DELAY);
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET);
    HAL_Delay(10);
}

void set_ch(int gain){
    if (gain == 8){
        gain = 5;
    }
    if (gain == 12){
        gain = 6;
    }
    for(int i = 0; i<CHANNELS; i++){
        send(69+i);
        send(0);
        send(0+(gain<<4));
        HAL_Delay(10);
    }
    for(int i = CHANNELS; i<8; i++){
        send(69+i);
        send(0);
        send(144);
        HAL_Delay(10);
    }

}

void read(long r[9]){
    int t = HAL_GetTick();
    while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_3)){
        HAL_Delay(25);
        char data[] = ".";
        HAL_UART_Transmit(&huart1, (uint8_t*)data, sizeof(data), 1000);

        if((t+1000)<HAL_GetTick()){
            char data[] = "Resetting \n";
            HAL_UART_Transmit(&huart1, (uint8_t*)data, sizeof(data), 1000);
            send(2);
            HAL_Delay(1);
            send(8);
//            ADC_setup();
            HAL_Delay(500);
            if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_3)) return;
            break;
        }
    }
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);
    for(int j =0; j<CHANNELS+1; j++){
        r[j] = 0;
        uint8_t data;
        HAL_SPI_Receive(&hspi1, &data, 1, 100);
        r[j] += data<<16;
        HAL_SPI_Receive(&hspi1, &data, 1, 100);
        r[j] += data<<8;
        HAL_SPI_Receive(&hspi1, &data, 1, 100);
        r[j] += data;
    }
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET);
    HAL_Delay(1);
}

void ADC_setup(){
    set_ch(1);
    HAL_Delay(1);
    send(0x41);
    send(0);
    send(0xD6);
    HAL_Delay(1);
    send(0x08);
    HAL_Delay(1);
}
